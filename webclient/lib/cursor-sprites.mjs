function blitSprite(canvas, rows, color, shadowColor = null) {
  const ctx = canvas.getContext("2d")
  ctx.clearRect(0, 0, canvas.width, canvas.height)
  const draw = (bits, col) => {
    for (let y = 0; y < rows.length; y++) {
      const [b0, b1, b2] = bits[y]
      let bit = 0
      for (const b of [b0, b1, b2]) {
        for (let i = 7; i >= 0; i--) {
          if ((b >> i) & 1) ctx.fillStyle = col, ctx.fillRect(bit, y, 1, 1)
          bit++
        }
      }
    }
  }
  if (shadowColor) draw(rows, shadowColor)
  draw(rows, color)
}

function makeSpritePair(iconRows, shadowRows, color) {
  const w = 24
  const h = 21
  const icon = makeCanvas(w, h)
  const shadow = makeCanvas(w, h)
  blitSprite(shadow, shadowRows, SHADOW_COLOR)
  blitSprite(icon, iconRows, color)
  return { icon, shadow }
}

let cache = null

export function cursorSpritePair(cursorState) {
  if (!cache) {
    cache = ICON_KEYS.map(([ik, sk], idx) =>
      makeSpritePair(SPRITE_BITS[ik], SPRITE_BITS[sk], ICON_COLORS[idx]))
  }
  const idx = Math.max(0, Math.min(cursorState, CURSOR_PUT))
  return cache[idx]
}

